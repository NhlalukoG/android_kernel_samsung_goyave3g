static void bma2x2_set_motion_interrupt(struct i2c_client *client,
	bool enable, bool factorytest)
{
	struct bma2x2_data *bma2x2 = i2c_get_clientdata(client);
	unsigned char data;

	pr_info("%s, enable(%d) factorytest(%d)\n",
		__func__, enable, factorytest);

	if (enable) {
		bma2x2_soft_reset(bma2x2->bma2x2_client);
		msleep(20);
		bma2x2_set_mode(bma2x2->bma2x2_client,
			BMA2X2_MODE_LOWPOWER1, BMA_ENABLED_INPUT);
		msleep(20);
		bma2x2_set_bandwidth(client, BMA2X2_BW_SET_REACTIVE_ALERT);
		bma2x2_set_range(client, BMA2X2_RANGE_SET);
		usleep_range(5000, 6000);

		data = 0x04;
		bma2x2_smbus_write_byte(client, BMA2X2_EN_INT1_PAD_SLOPE__REG,
			&data);
		usleep_range(2900, 3000);

		/* 0x02: Count3(Ignore 2 interrupts), 0x03:Count4 */
		data =  0x02;
		bma2x2_smbus_write_byte(client, BMA2X2_SLOPE_DUR__REG, &data);
		usleep_range(2900, 3000);

		if (factorytest) {
			data = 0x00;
			bma2x2_smbus_write_byte(client,
				BMA2X2_SLOPE_THRES__REG, &data);
		} else {
			data = 0x0a;
			bma2x2_smbus_write_byte(client,
				BMA2X2_SLOPE_THRES__REG, &data);
		}
		usleep_range(2900, 3000);

		data = 0x04;
		bma2x2_smbus_write_byte(client, BMA2X2_INT_ENABLE1_REG, &data);
	} else {
		data = 0x00;
		bma2x2_smbus_write_byte(client,
			BMA2X2_EN_INT1_PAD_SLOPE__REG, &data);
		usleep_range(1900, 2000);

		data = 0x03;
		bma2x2_smbus_write_byte(client, BMA2X2_SLOPE_DUR__REG, &data);
		usleep_range(1900, 2000);

		data = 0x00;
		bma2x2_smbus_write_byte(client, BMA2X2_INT_ENABLE1_REG, &data);
		usleep_range(1900, 2000);

		data = 0xff;
		bma2x2_smbus_write_byte(client, BMA2X2_SLOPE_THRES__REG,
			&data);
		usleep_range(1900, 2000);

		if (atomic_read(&bma2x2->factory_test)) {
			atomic_set(&bma2x2->factory_test, 0);
			bma2x2_set_mode(bma2x2->bma2x2_client,
				BMA2X2_MODE_NORMAL, BMA_ENABLED_INPUT);
		} else {
			bma2x2_set_mode(bma2x2->bma2x2_client,
				BMA2X2_MODE_SUSPEND, BMA_ENABLED_INPUT);
		}
		usleep_range(5000, 6000);
	}
}
