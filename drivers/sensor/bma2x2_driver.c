{
	struct i2c_client *client = to_i2c_client(dev);
	struct bma2x2_data *bma2x2 = i2c_get_clientdata(client);
	bool onoff;
	bool factory_test = false;
	unsigned long value = 0;
#if defined(DEFENCE_REACTIVE_ALERT)
	int this_cpu;
	unsigned long long t;
	unsigned long nanosec_rem;
#endif

	if (kstrtoul(buf, 10, &value)) {
		pr_err("%s: invalid value %lu\n", __func__, value);
		return -EINVAL;
	}

	/* mode of motion interrupt
	0. disable motion interrupt
	1. normal motion interrupt
	2. probe interrupt pin
	*/
	switch (value) {
	case 0:
		onoff = false;
		break;
	case 1:
		onoff = true;
		break;
	case 2:
		onoff = true;
		factory_test = true;
		atomic_set(&bma2x2->factory_test, 1);
		break;
	default:
		pr_err("%s: invalid value %lu\n", __func__, value);
		return count;
	}

	if (bma2x2->IRQ) {
		if (!value) {
			disable_irq_wake(bma2x2->IRQ);
			disable_irq(bma2x2->IRQ);
		} else {
			enable_irq(bma2x2->IRQ);
			enable_irq_wake(bma2x2->IRQ);
		}
	}

	mutex_lock(&bma2x2->data_mutex);
	atomic_set(&bma2x2->reactive_enable, onoff);
	atomic_set(&bma2x2->reactive_state, false);
	usleep_range(900, 1000);

#if defined(DEFENCE_REACTIVE_ALERT)
	this_cpu = raw_smp_processor_id();
	t = cpu_clock(this_cpu);
	nanosec_rem = do_div(t, 1000000000);
	/*pr_info("%s: ======this_cpu:%d [%5lu.%06lu]\n",
		__func__, this_cpu, (unsigned long)t, nanosec_rem / 1000);*/
	t_before = t;
	nanosec_before = nanosec_rem;
#endif
	if (bma2x2->IRQ)
		bma2x2_set_motion_interrupt(bma2x2->bma2x2_client, onoff,
			factory_test);

	usleep_range(900, 1000);
	mutex_unlock(&bma2x2->data_mutex);

	pr_info("%s: onoff = %d, state =%d\n", __func__,
		atomic_read(&bma2x2->reactive_enable),
		atomic_read(&bma2x2->reactive_state));

	return count;
}
