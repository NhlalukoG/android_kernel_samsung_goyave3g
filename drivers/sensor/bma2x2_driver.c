static int bma2x2_parse_dt(struct bma2x2_data *data, struct device *dev)
{
	struct device_node *np = dev->of_node;
	enum of_gpio_flags flags;
	int ret;

	if (np == NULL) {
		pr_err("%s, no dev_node\n", __func__);
		return -ENODEV;
	}

	data->acc_int1 = of_get_named_gpio_flags(np, "bma2x2,gpio_int1", 0,
		&flags);
	if (data->acc_int1 < 0) {
		pr_err("%s, get gpio_int error\n", __func__);
		return -ENODEV;
	}

	ret = of_property_read_u32(np, "bma2x2,accel_place", &data->place);
	if (ret) {
		dev_err(dev,
			"error reading property acc_place from the node %d\n",
			data->place);
		return ret;
	}

	data->reg_vdd = devm_regulator_get(dev, "bma2x2,vdd");
	if (IS_ERR(data->reg_vdd)) {
		pr_err("%s, could not get vdd, %ld\n",
			__func__, PTR_ERR(data->reg_vdd));
	} else {
		if (!((AVG_VIO <= regulator_get_voltage(data->reg_vdd))
			&& (regulator_get_voltage(data->reg_vdd) <= MAX_VDD))) {
			ret = regulator_set_voltage(data->reg_vdd,
				AVG_VIO, AVG_VIO);
			if (ret) {
				pr_err("%s, set voltage failed on vdd, rc=%d\n",
					__func__, ret);
				devm_regulator_put(data->reg_vdd);
				return ret;
			}
		}
	}
	
#ifdef CONFIG_SEC_USE_DEVICE_NODE_NAME
	ret = of_property_read_string(np, "device_node_name", &data->device_node_name);
	if(ret) {
		pr_err("%s, Could not read device node name, err=%d\n", ret);
		return ret;
	}
#endif
	
	return 0;
}
