static ssize_t bma2x2_delay_store(struct device *dev,
	struct device_attribute *attr,
	const char *buf, size_t count)
{
	unsigned long data;
	int error;
	int bw = 0;
	struct i2c_client *client = to_i2c_client(dev);
	struct bma2x2_data *bma2x2 = i2c_get_clientdata(client);

	error = kstrtoul(buf, 10, &data);
	if (error)
		return error;

	data = data / 1000000L;

	if (data > BMA2X2_MAX_DELAY)
		data = BMA2X2_MAX_DELAY;
	pr_info("%s [%d]\n", __func__, (int)data);

	atomic_set(&bma2x2->delay, (unsigned int) data);

	/*set bandwidth */
	switch (data) {
	case 0:
	case 1:
	case 10: /*100Hz*/
		bw = BMA2X2_BW_62_50HZ;
		break;
	case 20: /*50Hz*/
		bw = BMA2X2_BW_31_25HZ;
		break;
	case 50:
	case 66: /*20Hz*/
		bw = BMA2X2_BW_15_63HZ;
		break;
	case 200: /*5Hz*/
		bw = BMA2X2_BW_7_81HZ;
		break;
	default:
		break;
	}

	if (bw >= 0x08) {
		error = bma2x2_set_bandwidth(bma2x2->bma2x2_client,
			(unsigned char)bw);
		if (error < 0) {
			pr_err("%s, failed to set bandwidth\n", __func__);
			return -EINVAL;
		}
	}

	return count;
}


