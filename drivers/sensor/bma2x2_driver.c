static ssize_t bma2x2_calibration_show(struct device *dev,
	struct device_attribute *attr, char *buf)
{
	int cal_data[3];
	int err;
	mm_segment_t old_fs;
	struct file *cal_filp = NULL;
	int result = 1;

	old_fs = get_fs();
	set_fs(KERNEL_DS);

	cal_filp = filp_open(CALIBRATION_FILE_PATH,
		O_RDONLY, 0666);
	if (IS_ERR(cal_filp)) {
		pr_err("[ACC] %s: Can't open calibration file\n",
			__func__);
		set_fs(old_fs);
		err = PTR_ERR(cal_filp);
		return err;
	}
	err = cal_filp->f_op->read(cal_filp,
		(char *)cal_data,
		3 * sizeof(int), &cal_filp->f_pos);
	if (err != 3 * sizeof(int)) {
		pr_err("[ACC] %s: Can't read the cal data from file\n",
			__func__);
		filp_close(cal_filp, current->files);
		set_fs(old_fs);
		return -EIO;
	}

	filp_close(cal_filp, current->files);
	set_fs(old_fs);

	if (((cal_data[0] == 0) && (cal_data[1] == 0) && (cal_data[2] == 0)))
		result = 0;

	pr_debug("bma2x2_calibration_show %d  %d %d %d\n", result,
					cal_data[0], cal_data[1], cal_data[2]);
	return snprintf(buf, PAGE_SIZE, "%d %d %d %d\n",
		result, cal_data[0], cal_data[1], cal_data[2]);
}

static ssize_t bma2x2_calibration_store(struct device *dev,
	struct device_attribute *attr,
	const char *buf, size_t count)
{
	struct i2c_client *client = to_i2c_client(dev);
	struct bma2x2_data *bma2x2 = i2c_get_clientdata(client);
	int cal_data[3] = {0,};
	unsigned long data;
	signed char tmp;
	unsigned char timeout = 0;
	short acc_value_z = 0;
	unsigned char offset_target_z = 0;
	int err;
	mm_segment_t old_fs;
	struct file *cal_filp = NULL;

	err = kstrtoul(buf, 10, &data);
	if (err)
		return err;

	if (data) {
		/* check the current z value,set offset_target_z based on it */
		bma2x2_read_accel_z(bma2x2->bma2x2_client, bma2x2->sensor_type,
			&acc_value_z);
		pr_debug("acc_value_z = [%d], while accel calibration\n",
			acc_value_z);
		if (acc_value_z > 0)
			offset_target_z = 1;
		else
			offset_target_z = 2;

		/* x axis fast calibration */
		err = bma2x2_set_offset_target(bma2x2->bma2x2_client, 1,
			(unsigned char)0);
		if (err < 0)
			return -EINVAL;

		err = bma2x2_set_cal_trigger(bma2x2->bma2x2_client, 1);
		if (err < 0)
			return -EINVAL;

		do {
			usleep_range(2000, 2100);
			bma2x2_get_cal_ready(bma2x2->bma2x2_client, &tmp);
			timeout++;
			if (timeout == 50) {
				pr_err("[ACC] %s: get fast cal ready error\n",
					__func__);
				return -EINVAL;
			};
		} while (tmp == 0);

		/* y axis fast calibration */
		err = bma2x2_set_offset_target(bma2x2->bma2x2_client, 2,
			(unsigned char)0);
		if (err < 0)
			return -EINVAL;

		err = bma2x2_set_cal_trigger(bma2x2->bma2x2_client, 2);
		if (err < 0)
			return -EINVAL;

		do {
			usleep_range(2000, 2100);
			bma2x2_get_cal_ready(bma2x2->bma2x2_client, &tmp);
			timeout++;
			if (timeout == 50) {
				pr_err("[ACC] %s: get fast cal ready error\n",
					__func__);
				return -EINVAL;
			};
		} while (tmp == 0);

		/* z axis fast calibration */
		/* check the current z value,set offset_target_z based on it*/
		if (bma2x2_set_offset_target(bma2x2->bma2x2_client,
						3, offset_target_z) < 0)
			return -EINVAL;

		if (bma2x2_set_cal_trigger(bma2x2->bma2x2_client, 3) < 0)
			return -EINVAL;

		do {
			usleep_range(2000, 2100);
			bma2x2_get_cal_ready(bma2x2->bma2x2_client, &tmp);
			timeout++;
			if (timeout == 50) {
				pr_err("[ACC] %s: get fast cal ready error\n",
					__func__);
				return -EINVAL;
			};
		} while (tmp == 0);

		/* calibration */
		bma2x2_get_offset_x(bma2x2->bma2x2_client,
					(unsigned char *)&cal_data[0]);
		bma2x2_get_offset_y(bma2x2->bma2x2_client,
					(unsigned char *)&cal_data[1]);
		bma2x2_get_offset_z(bma2x2->bma2x2_client,
					(unsigned char *)&cal_data[2]);

		pr_info("%s, (%d,%d,%d)", __func__,
			cal_data[0], cal_data[1], cal_data[2]);

		old_fs = get_fs();
		set_fs(KERNEL_DS);
		cal_filp = filp_open(CALIBRATION_FILE_PATH,
		O_CREAT | O_TRUNC | O_WRONLY, 0666);
		if (IS_ERR(cal_filp)) {
			pr_err("[ACC] %s: Can't open calibration file\n",
							__func__);
			set_fs(old_fs);
			err = PTR_ERR(cal_filp);
			return err;
		}

		err = cal_filp->f_op->write(cal_filp,
					(char *)cal_data,
					3 * sizeof(int), &cal_filp->f_pos);
		if (err != 3 * sizeof(int)) {
			pr_err("[ACC] %s: Can't write the cal data to file\n",
								__func__);
			err = -EIO;
		}
		filp_close(cal_filp, current->files);
		set_fs(old_fs);
	} else {
		/* erase cal data */
		old_fs = get_fs();
		set_fs(KERNEL_DS);
		cal_filp = filp_open(CALIBRATION_FILE_PATH,
			O_CREAT | O_TRUNC | O_WRONLY, 0666);
		if (IS_ERR(cal_filp)) {
			pr_err("[ACC] %s: Can't open calibration file\n",
				__func__);
			set_fs(old_fs);
			err = PTR_ERR(cal_filp);
			return err;
		}
		cal_data[0] = 0;
		cal_data[1] = 0;
		cal_data[2] = 0;

		err = cal_filp->f_op->write(cal_filp,
			(char *)cal_data,
			3 * sizeof(int), &cal_filp->f_pos);
		if (err != 3 * sizeof(int)) {
			pr_err("[ACC] %s: Can't write the cal data to file\n",
				__func__);
			err = -EIO;
		}
		filp_close(cal_filp, current->files);
		set_fs(old_fs);

		bma2x2_set_offset_x(bma2x2->bma2x2_client,
			(unsigned char)cal_data[0]);
		bma2x2_set_offset_y(bma2x2->bma2x2_client,
			(unsigned char)cal_data[1]);
		bma2x2_set_offset_z(bma2x2->bma2x2_client,
			(unsigned char)cal_data[2]);
	}
	pr_debug("[ACC] cal_data (%d,%d,%d)===== end",
		cal_data[0], cal_data[1], cal_data[2]);

	return count;
}

